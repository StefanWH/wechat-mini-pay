package so.sao.wechat.mini.pay.errorcode;

public enum PaymentErrorCode implements ErrorCode {

    WAITING_FOR_USER_PAYMENT(10001), WAITING_FOR_QUERY(10002), UNKNOWN_ERROR(11001), PERMISSION_DENIED(12001),
    PARAMETER_ERROR(12002), INVALID_APP_ID(12003), INVALID_ORDER(13001), EXISTING_TRANSACTION(13002), ORDER_PAID(13003),
    ORDER_CLOSED(13004), ORDER_CANCELLED(13005), AUTH_CODE_INVALID(13006), BUYER_BALANCE_NOT_ENOUGH(13007),
    BUYER_NOT_MATCH(13008), ERROR_BALANCE_PAYMENT_DISABLE(13009), INVALID_BUYER(13010), PULL_MOBILE_CASHIER_FAIL(13011),
    MOBILE_PAYMENT_SWITCH_OFF(13012), PAYMENT_FAIL(13013), CARD_NOT_SUPPORTED(13014), MERCHANT_STATUS_ABNORMAL(13015),
    INVALID_PAYMENT(13016), USER_FACE_PAYMENT_SWITCH_OFF(13017);

    private int id;

    private PaymentErrorCode(int id) {
        this.id = id;
    }

    @Override
    public int getId() {
        return id;
    }
}

